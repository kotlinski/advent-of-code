import LaboratoriesSolver from '../solver.js';
import { describe, it } from 'node:test';
import { expect } from 'expect';

const input =
  '.......S.......\n' +
  '...............\n' +
  '.......^.......\n' +
  '...............\n' +
  '......^.^......\n' +
  '...............\n' +
  '.....^.^.^.....\n' +
  '...............\n' +
  '....^.^...^....\n' +
  '...............\n' +
  '...^.^...^.^...\n' +
  '...............\n' +
  '..^...^.....^..\n' +
  '...............\n' +
  '.^.^.^.^.^...^.\n' +
  '...............\n\n';

describe('day 7', () => {
  let solver: LaboratoriesSolver;
  describe('parser', () => {
    it('should parse the input', () => {
      solver = new LaboratoriesSolver(input);
      expect(solver.input).toEqual([
        ['.', '.', '.', '.', '.', '.', '.', 'S', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '^', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '^', '.', '^', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '^', '.', '^', '.', '^', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '^', '.', '^', '.', '.', '.', '^', '.', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '.', '^', '.', '^', '.', '.', '.', '^', '.', '^', '.', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '.', '^', '.', '.', '.', '^', '.', '.', '.', '.', '.', '^', '.', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
        ['.', '^', '.', '^', '.', '^', '.', '^', '.', '^', '.', '.', '.', '^', '.'],
        ['.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'],
      ]);
    });
  });
  describe('part one', () => {
    it(`should equal to 21`, () => {
      solver = new LaboratoriesSolver(input);
      const result = solver.solvePartOne();
      expect(result).toEqual(21);
    });
  });
  describe('part two', () => {
    it(`should equal to 4711`, () => {
      solver = new LaboratoriesSolver(input);
      const result = solver.solvePartTwo();
      expect(result).toEqual(4711);
    });
  });
});
